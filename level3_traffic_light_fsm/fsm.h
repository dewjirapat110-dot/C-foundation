// ============================================================
// fsm.h
// Public API ของ Finite State Machine สำหรับไฟจราจร
// ============================================================
//
// FSM (Finite State Machine) คืออะไร?
// = ระบบที่อยู่ใน "หนึ่งสถานะ" จากชุดสถานะที่จำกัด
//   และมีกฎการ "เปลี่ยนสถานะ" ที่ชัดเจน
//
// ตัวอย่างในชีวิตจริง:
//   - ไฟจราจร: แดง → เขียว → เหลือง → แดง...
//   - ปุ่มไฟห้อง: OFF → ON → OFF...
//   - เครื่องซักผ้า: รอ → ใส่น้ำ → ซัก → ปั่น → จบ
//
// ทำไมต้องใช้ FSM ในงาน firmware?
// = เพราะ firmware ส่วนใหญ่คือระบบที่ตอบสนองต่อ event
//   ถ้าเขียน if-else ซ้อนกันลึกๆ ระบบจะดูแลยากมาก
//   FSM บังคับให้เราคิดเป็น "สถานะ" ทำให้ตรรกะชัดเจน
// ============================================================

#ifndef FSM_H
#define FSM_H

#include <stdint.h>

// ============================================================
// State enum — นิยามสถานะที่เป็นไปได้
// ============================================================
//
// ใช้ enum แทน #define เพราะ:
//   1. compiler รู้ว่าค่าพวกนี้เป็นกลุ่มเดียวกัน → debug ง่ายกว่า
//   2. ใช้เป็น index ของ array ได้ตรงๆ (ค่าเริ่มจาก 0)
//   3. STATE_COUNT ตอนท้ายคือ "trick" ให้ได้จำนวน state อัตโนมัติ
//      ถ้าเพิ่ม state ใหม่ STATE_COUNT จะเพิ่มเองโดยไม่ต้องไปนับมือ
typedef enum
{
    STATE_RED    = 0,
    STATE_GREEN  = 1,
    STATE_YELLOW = 2,
    STATE_COUNT          // ← ค่านี้จะเท่ากับ 3 อัตโนมัติ
} TrafficState;

// ============================================================
// StateConfig — กำหนดข้อมูลของแต่ละ state
// ============================================================
//
// แต่ละ state มีคุณสมบัติ 3 อย่าง:
//   1. ชื่อ (เพื่อ debug print)
//   2. ระยะเวลาที่อยู่ใน state นี้ (วัดเป็น tick)
//   3. ฟังก์ชันที่จะรันตอนเข้า state นี้ (function pointer)
//   4. state ถัดไปที่จะเปลี่ยนไป
//
// Function pointer คืออะไร?
// = "ตัวแปรที่เก็บที่อยู่ของฟังก์ชัน"
// แทนที่จะเขียน:
//   if (state == GREEN) on_enter_green();
//   else if (state == YELLOW) on_enter_yellow();
//   ...
// เราเก็บ pointer ของฟังก์ชันไว้ในตาราง แล้วเรียก:
//   state_table[state].on_enter();
// → ลด if-else ที่จะยาวขึ้นตามจำนวน state
typedef struct
{
    const char   *name;          // ชื่อ state เพื่อ print
    uint32_t      duration;      // อยู่ state นี้กี่ tick แล้วเปลี่ยน
    void        (*on_enter)(void);  // ← function pointer: รันตอนเข้า state
    TrafficState  next_state;    // state ที่จะเปลี่ยนไปเมื่อหมดเวลา
} StateConfig;

// ============================================================
// FSM — "กล่อง" ที่ห่อสถานะปัจจุบันของ machine ไว้
// ============================================================
//
// ทำไมต้องห่อใน struct (ไม่ใช้ global variable)?
// = เพื่อให้สร้าง machine หลายตัวได้ในโปรแกรมเดียว
//   เช่น สี่แยก 2 จุดใช้ FSM 2 ตัว แต่ใช้ตาราง state เดียวกัน
typedef struct
{
    TrafficState current_state;  // อยู่ state ไหนตอนนี้
    uint32_t     tick_count;     // อยู่ใน state นี้มากี่ tick แล้ว
} FSM;

// ============================================================
// API
// ============================================================

// เริ่มต้น FSM ด้วย state เริ่มต้นที่กำหนด
// จะเรียก on_enter ของ state นั้นด้วย
void fsm_init(FSM *fsm, TrafficState initial_state);

// "เดินเครื่อง" 1 tick — เพิ่ม tick_count
// ถ้าครบเวลาของ state ปัจจุบัน จะเปลี่ยนไป state ถัดไป
//
// ใน firmware จริง ฟังก์ชันนี้ถูกเรียกจาก timer interrupt
// (ทุกๆ 1ms, 10ms, 100ms — แล้วแต่ออกแบบ)
void fsm_tick(FSM *fsm);

// คืนชื่อของ state ปัจจุบัน (สำหรับ debug print)
const char *fsm_current_name(const FSM *fsm);

#endif // FSM_H
